#include "iostream"
using namespace std;

int main()
{
	//整型
	//1、短整型   2字节
	short a = 10;

	//2、整型     4字节
	int b = 10;

	//3、长整型   4字节
	long c = 10;

	//4、长长整型 8字节
	long long d = 10;

	cout << "a =" << a << endl;
	cout << "b =" << b << endl;
	cout << "c =" << c << endl;
	cout << "d =" << d << endl;

	system("pause");

	return 0;
}
